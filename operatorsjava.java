// arethemetic operators

public class operatorsjava {
    public static void main(String args[])
    {
        int num1=60 , num2=50;
        System.out.println(num1+num2); // next line print
        System.out.print(num1+num2); // print on same line
        System.out.println(num1*num2);  //multiply operator
        System.out.println(num1%num2); // modulo or mod operator for remainder
        System.out.println(num1++); //increment operator
        System.out.println(num1--); //decrement operator print arethmetic data then decrement by 1
        System.out.println(--num1);       // decrement by 1 first then print the arethemetic data
    }   
}
